
def area(a):
    """
    Вычисляет площадь квадрата.
    
    Parameters:
        a (float): Длина стороны квадрата
        
    Returns:
        float: Площадь квадрата по формуле a * a
    """
    return a * a


def perimeter(a):
    """
    Вычисляет периметр квадрата.
    
    Parameters:
        a (float): Длина стороны квадрата
        
    Returns:
        float: Периметр квадрата по формуле 4 * a
    """
    return 4 * a