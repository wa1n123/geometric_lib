def area(a, h):
    """
    функция по поиску площади треугольника
    входные данные: высота и основание треугольника
    возвращает его площадь
    """
    return a * h / 2

def perimeter(a, b, c):
    """
    функция по поиску площади треугольника
    входные данные: стороны треугольника
    возвращает его периметр
    """
    return a + b + c
