def area(a, b):
    """
    Вычисляет площадь прямоугольника.

    Parameters:
        a (float): Длина первой стороны прямоугольника
        b (float): Длина второй стороны прямоугольника

    Returns:
        float: Площадь прямоугольника по формуле a * b
    """
    return a * b

def perimeter(a, b):
    """
    Вычисляет периметр прямоугольника.
    
    Parameters:
        a (float): Длина первой стороны прямоугольника
        b (float): Длина второй стороны прямоугольника
        
    Returns:
        float: Периметр прямоугольника по формуле 2 * (a + b)
    """
    return (a + b) * 2
